    public void daftar() {
        try {
            koneksi objkoneksi = new koneksi();
            Connection con = (Connection) koneksi.bukakoneksi();
            Statement stmt = (Statement) con.createStatement (
                    ResultSet.TYPE_SCROLL_INSENSITIVE,
                    ResultSet.CONCUR_UPDATABLE);
            String query = "insert into kunjungan values"
                    + "('" + txt_kode_kunjungan.getText() + "','" + txt_tanggal_kunjungan.getText() + "',"
                    + "'" + txt_petugas.getText() + "')";
            int rows = stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Data telah diterima", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
           new FormPeriksa().show();
            this.dispose();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    
    
    

private void txt_kode_kunjunganKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_kode_kunjunganKeyReleased
// TODO add your handling code here:
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        DecimalFormat dcf = new DecimalFormat("00");
        Calendar now = Calendar.getInstance();
        int hrs = now.get(Calendar.HOUR_OF_DAY);
        int min = now.get(Calendar.MINUTE);
        int sec = now.get(Calendar.SECOND);
        
        txt_tanggal_kunjungan.setText(String.valueOf(df.format(Calendar.getInstance().getTime()) 
                + ", " + dcf.format(hrs) + ":" + dcf.format(min)));
}//GEN-LAST:event_txt_kode_kunjunganKeyReleased

private void btn_daftar_kunjunganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_daftar_kunjunganActionPerformed
// TODO add your handling code here:
    String kode, kunjungan, petugas;
    kode = txt_kode_kunjungan.getText();
    petugas = txt_petugas.getText();
    
    try {
        koneksi objkoneksi = new koneksi();
        Connection con = (Connection) koneksi.bukakoneksi();
        
        if (kode.equals("") || kode.length() < 1) {
            JOptionPane.showMessageDialog(null, "Kode Pasien Harus Diisi!", "Warning",
                    JOptionPane.INFORMATION_MESSAGE);
            txt_kode_kunjungan.requestFocus();
        } else if (kode.length() > 5){
            JOptionPane.showMessageDialog(null,"Kode hanya terdiri dari 5 karakter!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            txt_kode_kunjungan.requestFocus();
        } else if (petugas.equals("") || petugas.length() < 1) {
            JOptionPane.showMessageDialog(null, "Nama Petugas Harus Diisi!", "Warning",
                    JOptionPane.INFORMATION_MESSAGE);
            txt_petugas.requestFocus();
        } else {
            daftar();
        }
    } catch (SQLException e) {
        
    }
}//GEN-LAST:event_btn_daftar_kunjunganActionPerformed


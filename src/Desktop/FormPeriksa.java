    public void kirim(){
        Object[] options = {"Ya", "Batal"};
        if (JOptionPane.showOptionDialog(null, "Anda yakin keluhan dan resep sudah diisi dengan benar?","Warning",
                JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,null, options, options[0])
                == 0) {
        try {
            koneksi objkoneksi = new koneksi();
            Connection con = (Connection) koneksi.bukakoneksi();
            Statement stmt = (Statement) con.createStatement(
                    ResultSet.TYPE_SCROLL_INSENSITIVE,
                    ResultSet.CONCUR_UPDATABLE);
            String query = "insert into periksa values"
                    + "('" + kode_periksa.getText() + "','" + txta_keluhan.getText() + "',"
                    + "'" + txta_resep.getText() + "','" + txt_diagnosa.getText() + "',"
                    + "'" + txt_pemeriksa.getText() + "','" + "','1')";
            int rows = stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Data telah terkirim", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            kode_periksa.setText("");
            reset();
            new Apotek().show();
            this.dispose();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    }
      

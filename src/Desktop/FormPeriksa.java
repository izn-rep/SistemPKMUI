    public void bukaobat(){
        DefaultTableModel MapTablePeriksa = new DefaultTableModel();
        MapTablePeriksa.addColumn("Nama Obat");
        MapTablePeriksa.addColumn("Stock");
        
        try {
            String query = "select * from db_obat";
            koneksi objkoneksi = new koneksi();
            Connection con = (Connection) koneksi.bukakoneksi();
            Statement stat = (Statement) con.createStatement();
            ResultSet rs = stat.executeQuery(query);
            while (rs.next()) {
                MapTablePeriksa.addRow(new Object[]{rs.getString(2),rs.getString(4)});
                
            }
            table_obat.setModel(MapTablePeriksa);
        } catch (Exception e) {
            
        }
    }


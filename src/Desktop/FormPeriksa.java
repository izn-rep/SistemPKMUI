private void btn_simpan_periksaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpan_periksaActionPerformed
// TODO add your handling code here:
    String kode, keluhan, resep, diagnosa, pemeriksa;
    kode = kode_periksa.getText();
    keluhan = txta_keluhan.getText();
    resep = txta_resep.getText();
    diagnosa = txt_diagnosa.getText();
    pemeriksa = txt_pemeriksa.getText();
    
    try {
        koneksi objkoneksi = new koneksi();
        Connection con = (Connection) koneksi.bukakoneksi();
        
        if (kode.equals("") || kode.length() < 1) {
            JOptionPane.showMessageDialog(null, "Kode Pasien harus diisi!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            kode_periksa.requestFocus();
        } else if (kode.length() > 5){
            JOptionPane.showMessageDialog(null,"Kode hanya terdiri dari 5 karakter!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            kode_periksa.requestFocus();
        } else if (keluhan.equals("") || keluhan.length() < 1) {
            JOptionPane.showMessageDialog(null, "Keluhan Pasien agar diisi!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            txta_keluhan.requestFocus();
        } else if (resep.equals("") || resep.length() < 1) {
            JOptionPane.showMessageDialog(null, "Resep Dokter harus diisi!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            txta_resep.requestFocus();
        } else if (diagnosa.equals("") || diagnosa.length() < 1) {
            JOptionPane.showMessageDialog(null, "Diagnosa Dokter harus diisi!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            txt_diagnosa.requestFocus();
        } else if (pemeriksa.equals("") || pemeriksa.length() < 1) {
            JOptionPane.showMessageDialog(null, "Nama Dokter harus diisi!", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
            txt_pemeriksa.requestFocus();
        } else {
            kirim();
        }
    } catch (SQLException e) {
        
    }
}//GEN-LAST:event_btn_simpan_periksaActionPerformed

